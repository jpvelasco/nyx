var (
	omadaController string
	omadaUsername   string
	omadaPassword   string
	omadaSite       string
	omadaOutFile    string
	omadaDebug      bool
)

var omadaCmd = &cobra.Command{
	Use:   "omada",
	Short: "Omada SDN controller integration",
	Long:  "Commands for importing configuration and running audits against an Omada SDN controller.",
}

// -----------------------------------------------------------------------
// omada import
// -----------------------------------------------------------------------

var omadaImportCmd = &cobra.Command{
	Use:   "import",
	Short: "Import network topology from Omada controller and generate a spec",
	Long: `Connects to your Omada controller, reads VLANs, ACL rules, and
connected clients, then generates a nyx spec YAML file.

Credentials can be supplied via flags or environment variables:
  OMADA_USERNAME  — controller admin username
  OMADA_PASSWORD  — controller admin password
  OMADA_HOST      — controller IP or hostname (overrides --controller)`,
	Example: `  nyx omada import --controller 192.168.0.253 --out homelab.yaml
  OMADA_USERNAME=admin OMADA_PASSWORD=secret nyx omada import --controller 192.168.0.253`,
	RunE: func(cmd *cobra.Command, args []string) error {
		host, user, pass, err := resolveOmadaCreds(omadaController, omadaUsername, omadaPassword)
		if err != nil {
			return err
		}

		dur, parseErr := time.ParseDuration(timeout)
		if parseErr != nil {
			dur = 30 * time.Second
		}
		ctx, cancel := context.WithTimeout(context.Background(), dur)
		defer cancel()

		fmt.Fprintf(os.Stderr, "Connecting to Omada controller at %s...\n", host)

		result, err := omada.ImportSpec(ctx, host, user, pass, omadaSite, omadaDebug)
		if err != nil {
			return fmt.Errorf("import failed: %w", err)
		}

		// Print warnings to stderr so they don't pollute the YAML output
		for _, w := range result.Warnings {
			fmt.Fprintf(os.Stderr, "Warning: %s\n", w)
		}

		fmt.Fprintf(os.Stderr, "Imported from site %q: %d networks, %d ACL rules, %d connected clients\n",
			result.Site.Name, result.NetworkCount, result.ACLRuleCount, result.ClientCount)

		// Serialise spec to YAML
		specYAML, err := yaml.Marshal(result.Spec)
		if err != nil {
			return fmt.Errorf("serializing spec: %w", err)
		}

		header := fmt.Sprintf("# Generated by nyx omada import\n# Controller: %s  Site: %s\n# Controller version: %s\n\n",
			host, result.Site.Name, result.ControllerVersion)

		out := []byte(header)
		out = append(out, specYAML...)

		if omadaOutFile != "" {
			if err := os.WriteFile(omadaOutFile, out, 0644); err != nil {
				return fmt.Errorf("writing spec to %s: %w", omadaOutFile, err)
			}
			fmt.Fprintf(os.Stderr, "Spec written to %s\n", omadaOutFile)
		} else {
			fmt.Print(string(out))
		}
		return nil
	},
}

