var omadaCheckCmd = &cobra.Command{
	Use:   "check",
	Short: "Import from Omada controller and immediately run a live audit",
	Long: `Imports network topology from the Omada controller, then runs a live
audit against your network to verify the actual behaviour matches the
configured intent.

This is equivalent to:
  nyx omada import --controller <host> | nyx audit --spec -

Credentials via flags or env vars (OMADA_USERNAME, OMADA_PASSWORD, OMADA_HOST).`,
	Example: `  nyx omada check --controller 192.168.0.253
  nyx omada check --controller 192.168.0.253 --json`,
	RunE: func(cmd *cobra.Command, args []string) error {
		host, user, pass, err := resolveOmadaCreds(omadaController, omadaUsername, omadaPassword)
		if err != nil {
			return err
		}

		dur, parseErr := time.ParseDuration(timeout)
		if parseErr != nil {
			dur = 300 * time.Second
		}
		ctx, cancel := context.WithTimeout(context.Background(), dur)
		defer cancel()

		fmt.Fprintf(os.Stderr, "Connecting to Omada controller at %s...\n", host)

		result, err := omada.ImportSpec(ctx, host, user, pass, omadaSite, omadaDebug)
		if err != nil {
			return fmt.Errorf("import failed: %w", err)
		}

		for _, w := range result.Warnings {
			fmt.Fprintf(os.Stderr, "Warning: %s\n", w)
		}

		fmt.Fprintf(os.Stderr, "Running audit for site %q (%d networks, %d assertions)...\n",
			result.Site.Name, result.NetworkCount, len(result.Spec.Assertions))

		engine := audit.NewEngine(result.Spec)
		auditReport, err := engine.Run(ctx)
		if err != nil {
			return fmt.Errorf("audit failed: %w", err)
		}

		w, err := getWriter()
		if err != nil {
			return err
		}
		if outputPath != "" {
			defer w.Close()
		}

		if jsonOutput {
			return report.RenderJSON(w, auditReport)
		}
		report.RenderHuman(w, auditReport)
		return nil
	},
}

