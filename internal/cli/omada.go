var omadaInfoCmd = &cobra.Command{
	Use:   "info",
	Short: "Show Omada controller version and connection info (no credentials required)",
	Example: `  nyx omada info --controller 192.168.0.253`,
	RunE: func(cmd *cobra.Command, args []string) error {
		host := omadaController
		if h := os.Getenv("OMADA_HOST"); h != "" && host == "" {
			host = h
		}
		if host == "" {
			return fmt.Errorf("--controller is required (or set OMADA_HOST)")
		}

		ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
		defer cancel()

		client, err := omada.NewClient(ctx, host)
		if err != nil {
			return fmt.Errorf("connecting to controller: %w", err)
		}

		info := client.Info()
		fmt.Printf("Controller : %s\n", host)
		fmt.Printf("Version    : %s\n", info.ControllerVer)
		fmt.Printf("API version: v%s\n", info.APIVer)
		fmt.Printf("OmadaCID   : %s\n", info.OmadaCID)
		fmt.Printf("Configured : %v\n", info.Configured)
		return nil
	},
}

