// resolveOmadaCreds returns the controller host, username, and password,
// preferring explicit flags over environment variables.
// Credentials are NEVER printed or logged.
func resolveOmadaCreds(flagHost, flagUser, flagPass string) (host, user, pass string, err error) {
	host = flagHost
	if h := os.Getenv("OMADA_HOST"); h != "" && host == "" {
		host = h
	}
	if host == "" {
		return "", "", "", fmt.Errorf("--controller is required (or set OMADA_HOST)")
	}

	user = flagUser
	if u := os.Getenv("OMADA_USERNAME"); u != "" && user == "" {
		user = u
	}
	if user == "" {
		return "", "", "", fmt.Errorf("--username is required (or set OMADA_USERNAME)")
	}

	pass = flagPass
	if p := os.Getenv("OMADA_PASSWORD"); p != "" && pass == "" {
		pass = p
	}
	if pass == "" {
		return "", "", "", fmt.Errorf("--password is required (or set OMADA_PASSWORD)")
	}

	return host, user, pass, nil
}

func init() {
	// Shared flags across omada subcommands
	for _, cmd := range []*cobra.Command{omadaImportCmd, omadaCheckCmd, omadaInfoCmd} {
		cmd.Flags().StringVar(&omadaController, "controller", "", "Omada controller IP or hostname")
		cmd.Flags().StringVar(&omadaSite, "site", "", "Omada site name (defaults to first site if omitted)")
	}
	for _, cmd := range []*cobra.Command{omadaImportCmd, omadaCheckCmd} {
		cmd.Flags().StringVar(&omadaUsername, "username", "", "Controller admin username (or set OMADA_USERNAME)")
		cmd.Flags().StringVar(&omadaPassword, "password", "", "Controller admin password (or set OMADA_PASSWORD)")
		cmd.Flags().BoolVar(&omadaDebug, "debug", false, "Print raw API responses to stderr for troubleshooting")
	}
	omadaImportCmd.Flags().StringVar(&omadaOutFile, "out", "", "Write spec YAML to this file (default: stdout)")

	omadaCmd.AddCommand(omadaImportCmd)
	omadaCmd.AddCommand(omadaCheckCmd)
	omadaCmd.AddCommand(omadaInfoCmd)
}
